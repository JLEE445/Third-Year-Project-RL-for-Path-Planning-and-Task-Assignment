import numpy as np
import matplotlib.pyplot as plt
from matplotlib.colors import TwoSlopeNorm

def plot_4_heatmaps(arrays, titles, vmin=None, vmax=None, vcenter=None):
    fig, axes = plt.subplots(4, 1, figsize=(10, 8))

    # Flatten axes for easy looping
    axes = axes.flatten()

    # If no range given, compute global min/max
    if vmin is None:
        vmin = min(np.min(a) for a in arrays)
    if vmax is None:
        vmax = max(np.max(a) for a in arrays)

    # Optional midpoint (for diverging colormap)
    if vcenter is not None:
        norm = TwoSlopeNorm(vmin=vmin, vcenter=vcenter, vmax=vmax)
    else:
        norm = None

    # Plot each heatmap
    for i, (ax, data, title) in enumerate(zip(axes, arrays, titles)):
        im = ax.imshow(data, cmap='RdYlBu_r', vmin=vmin, vmax=vmax, norm=norm)
        ax.set_title(title)
        ax.set_xlabel("X axis")
        ax.set_ylabel("Y axis")

    # Add ONE shared colorbar
    fig.colorbar(im, ax=axes, orientation='vertical', shrink=0.8)

    plt.tight_layout()
    plt.show()


